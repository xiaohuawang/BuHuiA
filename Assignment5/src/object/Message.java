package object;

public class Message {

	private int messageDetail;
	private double fractionNum;

	public int getMessageDetail() {
		return messageDetail;
	}

	public void setMessageDetail(int messageDetail) {
		this.messageDetail = messageDetail;
	}

	public double getFractionNum() {
		return fractionNum;
	}

	public void setFractionNum(double fractionNum) {
		this.fractionNum = fractionNum;
	}

}
